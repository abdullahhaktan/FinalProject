        public WriterMessageManager(IWriterMessageDal writerMessageDal)
        {
            _writerMessageDal = writerMessageDal;
        }

        public List<WriterMessage> GetListReceiverMessage(string mail)
        {
            return _writerMessageDal.GetbyFilter(item => item.Receiver == mail);
        }


